<?php

use DI\ContainerBuilder;
use Symfony\Component\Yaml\Yaml;

/*
|--------------------------------------------------------------------------
| Composer
|--------------------------------------------------------------------------
*/

require __DIR__.'/../vendor/autoload.php';

/*
|--------------------------------------------------------------------------
| Create the application
|--------------------------------------------------------------------------
*/

$app = new ContainerBuilder();
$app->addDefinitions([
	'Config' => Yaml::parseFile(__DIR__.'/../config.yaml')
]);

/*
|--------------------------------------------------------------------------
| Build our application
|--------------------------------------------------------------------------
*/

return $app->build();