<?php

use Wingman\Services\ConsoleService;
use Wingman\Console\Kernel;

/*
|--------------------------------------------------------------------------
| Console
|--------------------------------------------------------------------------
*/

$console = $wingman->get(ConsoleService::class);

/*
|--------------------------------------------------------------------------
| Bind our commands
|--------------------------------------------------------------------------
*/

foreach((new Kernel())->commands as $command) {
	$console->add($wingman->get($command));
}

/*
|--------------------------------------------------------------------------
| Return our instance
|--------------------------------------------------------------------------
*/

return $console;